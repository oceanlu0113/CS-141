public class Flower {

    public int leaves;
    public int petals;
    public String names;

    public Flower() {
        this.leaves = leaves;
        this.petals = petals;
        this.names = names;
    }
    
    public void setLeaves(int l) {
        leaves = l;
    }
    
    public void setPetals(int p) {
        petals = p;
    }
    
    public void setNames(String n) {
        names = n;
    }
    
    public int getLeaves() {
        return leaves;
    }
    
    public int getPetals() {
        return petals;
    }
    
    public String getName() {
        return names;
    }
    
}

